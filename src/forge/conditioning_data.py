def get_protocol(protocol_id: str) -> dict | None:
    """Look up a protocol by its ID (e.g. 'AC-001'). Returns None if not found."""
    return PROTOCOLS_BY_ID.get(protocol_id)


def get_protocols_by_system(system: str) -> list[dict]:
    """Return all protocols for a given energy system."""
    return [p for p in CONDITIONING_PROTOCOLS_DATA if p["system"] == system]


def get_protocols_by_tier(tier: str) -> list[dict]:
    """Return all protocols of a given tier ('A', 'B', or 'C')."""
    return [p for p in CONDITIONING_PROTOCOLS_DATA if p["tier"] == tier]


def recommend_protocol(goal: str, environment: str, time_min: int) -> str | None:
    """Return the best protocol_id for a goal + environment + time, or None if unavailable."""
    try:
        pid = CONDITIONING_DECISION_MAP[goal][environment][time_min]
        return pid if pid else None
    except KeyError:
        return None


